#include <vector>
#include <bits/stdc++.h>

int main()
{
    int numCord;
    std::vector<int[2]> cords;
    int x{};
    int y{};
    std::cin >> numCord;
    int param[2];
    for(int i = 0; i < numCord; i++)
    {
        std::cin >> x >> y;
        param[0] = x;
        param[1] = y;
        cords.push_back(param);
    }
    return 0;
}
